DATA SEGMENT
    NUM1 DD 11223344H
    CHECKSUM DD 12223344H
DATA ENDS
CODE SEGMENT   
    ASSUME CS:CODE,DS:DATA
    MOV AX,DATA
    MOV DS,AX
    MOV BX,WORD PTR NUM1
    XOR BX,WORD PTR CHECKSUM
    JZ NEXT
    JNZ EXIT
    NEXT: 
        MOV CX,WORD PTR 2[NUM1]
        XOR CX,WORD PTR 2[CHECKSUM]
        JZ COMPELETE
        JNZ EXIT
    COMPELETE :
        MOV AX,0000H
        INT 03
    EXIT: 
        MOV AX,1111H
        INT 03
CODE ENDS
END
