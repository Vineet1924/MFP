DATA SEGMENT
    SOURCE DB 05H,15H,25H,35H,45H
    DESTINATION DB 5 DUP(0)
DATA ENDS
CODE SEGMENT
    ASSUME CS:CODE,DS:DATA
    MOV AX,DATA
    MOV DS,AX
    MOV CX,05
    LEA SI,SOURCE
    LEA DI,DESTINATION
    MYLOOP :
        MOV AL,[SI]
        MOV [DI],AL
        INC DI
        INC SI
        DEC CX
        JZ EXIT
        JNZ MYLOOP
    EXIT :
        INT 03
    INT 03
CODE ENDS
END